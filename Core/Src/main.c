		  ADC_Variable[1] = hadc3.Instance->JDR1;
		  if((hadc3.Instance->ISR & ADC_ISR_JEOC) == ADC_ISR_JEOC)
		  {

			  ADC_Variable[1] = hadc3.Instance->JDR2;
		  }

